public class swtchsimple {
    public static void main(String[] args) {

        int switchValue = 2;
        switch (switchValue) {
            case 1:
                System.out.println("this is 1 number");
                break;
            case 2:
                System.out.println("this is 2 number");
                break;
            default:
                System.out.println("no number");
        }
    }
}

